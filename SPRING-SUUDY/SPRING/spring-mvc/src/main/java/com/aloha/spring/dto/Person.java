package com.aloha.spring.dto;

/**
 * 기본 생성자
 * 매개변수 생성자
 * getter, setter
 * toString
 */
public class Person {

	private String name;
	private int age;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
}
